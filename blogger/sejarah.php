<br>Pasukan ini dibentuk dengan jalan memperpolitik seni permainan burung gagak, yang dalam Bahasa Bali disebut “Magoak-goakan”. Dari permainan ini akhirnya terbentuknya pasukan Truna Goak yang berjumlah 2000 orang, yang terdiri dari para pemuda perwira berbadan tegap, tangkas, serta memiliki moral yang tinggi di bawah pimpinan perang yang bernama Ki Gusti Tamblang Sampun dan di wakili oleh Ki Gusti Made Batan.</br>

<br>Ki Gusti Ngurah Panji Sakti beserta putra-putra Baginda dan perwira lainnya, memimpin pasukan Truna Goak yang semuanya siap bertempur berangkat menuju daerah Blambang. Dalam pertempuran ini Raja Blambangan gugur di medan perang dengan demikian kerajaan Blambangan dengan seluruh penduduknya tunduk pada Raja Ki Gusti Ngurah Panji Sakti. Berita kemenangan ini segera di dengar oleh Raja Mataram Sri Dalem Solo dan kemudian beliau menghadiahkan seekor gajah dengan 3 orang pengembalanya kepada Ki Gusti Ngurah Panji Sakti. Menundukkan kerajaan Blambangan harus ditebus dengan kehilangan seorang putra Baginda bernama Ki Gusti Ngurah Panji Nyoman, hal mana mengakibatkan Baginda Raja selalu nampak bermuram durjan. Hanya berkat nasehat-nasehat Pandita Purohito, akhirnya kesedihan Baginda dapat terlupakan dan kemudian terkandung maksud untuk membangun istana yang baru di sebelah Utara Sukasada.

Pada sekitar tahun Candrasangkala “Raja Manon Buta Tunggal” atau Candrasangkala 6251 atau sama dengan tahun caka 1526 atau tahun 1604 Masehi, Ki Gusti Ngurah Panji Sakti memerintahkan rakyatnya membabat tanah untuk mendirikan sebuah istana di atas padang rumput alang-alang, yakni lading tempat pengembala ternak, dimana ditemukan orang-orang menanam Buleleng. Pada ladang Buleleng itu Baginda melihat beberapa buah pondok-pondok yang berjejer memanjang. Di sanalah beliau mendirikan istana yang baru, yang menurut perhitungan hari sangat baik pada waktu itu, jatuh pada tanggal “30 Maret 1604”.

Selanjutnya Istana Raja yang baru dibangun itu disebut “SINGARAJA” karena mengingat bahwa keperwiraan Raja Ki Gusti Ngurah Pnji Sakti tak ubahnya seperti Singa.
